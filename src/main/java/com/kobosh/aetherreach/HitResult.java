package com.kobosh.aetherreach;

public class HitResult {
    public final int x, y, z;
    public final int o;
    public final int f;

    public HitResult(int x, int y, int z, int o, int f) {
        this.x = x;
        this.y = y;
        this.z = z;
        this.o = o;
        this.f = f;
    }
}
